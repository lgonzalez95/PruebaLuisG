    @Override
    public void actionPerformed(ActionEvent evento) {
        if (evento.getSource() == jmiRegistrarEstudiante) {
            ventanaRegistro = new VentanaRegistroEstudiante();
            ventanaRegistro.init(registroEstudiantes);
            ventanaRegistro.setVisible(true);
        }
        if (evento.getSource() == jmiModificarEstudiante) {
            ventanaModificar = new VentanaModificarEstudiante();
            ventanaModificar.init(registroEstudiantes);
            ventanaModificar.setVisible(true);
        }
        if (evento.getSource() == jmiEliminarEstudiante) {
            ventanaEliminar = new VentanaEliminarEstudiante();
            ventanaEliminar.init(registroEstudiantes);
            ventanaEliminar.setVisible(true);
        }
        if (evento.getSource() == jmiConsultarEstudiantes) {
            ventanaConsultarEstudiante = new VentanaConsultarEstudiantes();
            ventanaConsultarEstudiante.init(registroEstudiantes);
            ventanaConsultarEstudiante.setVisible(true);
        }
        if (evento.getSource() == jmiRegistrarCursos) {
            ventanaRegistroCurso = new VentanaRegistroCurso();
            ventanaRegistroCurso.init(registroCursos);
            ventanaRegistroCurso.setVisible(true);
        }
        if (evento.getSource() == jmiModificarCursos) {
            ventanaModificarCurso = new VentanaModificarCurso();
            ventanaModificarCurso.init(registroCursos);
            ventanaModificarCurso.setVisible(true);
        }
        if (evento.getSource() == jmiEliminarCursos) {
            ventanaEliminarCursos = new VentanaEliminarCursos();
            ventanaEliminarCursos.init(registroCursos);
            ventanaEliminarCursos.setVisible(true);
        }
        if (evento.getSource() == jmiConsultarCursos) {
            ventanaConsultarCursos = new VentanaConsultarCursos();
            ventanaConsultarCursos.init(registroCursos);
            ventanaConsultarCursos.setVisible(true);
        }
        if(evento.getSource()==jmiProcesoDeMatricula){
            ventanaMatricula= new VentanaMatricula();
            ventanaMatricula.init(registroEstudiantes, registroCursos, registroMatricula);
            ventanaMatricula.setVisible(true);
        }
        if(evento.getSource()==jmiReportes){
            ventanaReportes = new VentanaReportes();
            ventanaReportes.init(registroMatricula);
            ventanaReportes.setVisible(true);
        }
        if (evento.getSource() == jmiSalir) {
            JOptionPane.showMessageDialog(null, "Hasta pronto");
            System.exit(0);
        }
    }
